def build_prompt(query: str, context_chunks: list[dict]) -> list[dict]:
    context_text = "\n\n".join(
        f"[{i + 1}] {c.get('text', '').strip()}"
        for i, c in enumerate(context_chunks)
    )
    return [
        {"role": "system", "content": SYSTEM_PROMPT},
        {
            "role": "user",
            "content": f"CONTEXT (نصاب سے):\n{context_text}\n\nسوال: {query}",
        },
    ]


def build_citations(chunks: list[dict]) -> list[dict]:
    citations = []
    for c in chunks:
        raw_text = c.get("text", "")
        if not raw_text:
            continue
        # Normalize whitespace while preserving Urdu RTL text integrity
        normalized = " ".join(raw_text.split())
        text_preview = normalized[:100] + ("..." if len(normalized) > 100 else "")
        citations.append(
            {
                "chunk_id":    c.get("chunk_id"),
                "chapter":     c.get("chapter"),
                "page":        c.get("page_start"),
                "page_start":  c.get("page_start"),
                "page_end":    c.get("page_end"),
                "source":      c.get("source"),
                "score":       c.get("score"),
                "text":        raw_text[:120],
                "text_preview": text_preview,
            }
        )
    return citations
